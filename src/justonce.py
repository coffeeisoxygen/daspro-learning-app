ANGKA_PERTAMA = 5000
ANGKA_KEDUA = 2000
ANGKA_KETIGA = 200
ANGKA_TAMBAHAN = 500

# Step 1
angka_pertama = ANGKA_PERTAMA + ANGKA_TAMBAHAN
print(f"{ANGKA_PERTAMA} + {ANGKA_TAMBAHAN} = {angka_pertama}")

# Step 2
angka_kedua = ANGKA_KEDUA + ANGKA_TAMBAHAN
print(f"{ANGKA_KEDUA} + {ANGKA_TAMBAHAN} = {angka_kedua}")

# Step 3
jumlah = angka_pertama + angka_kedua - ANGKA_KETIGA
print(f"{angka_pertama} + {angka_kedua} - {ANGKA_KETIGA} = {jumlah}")
